#include<stdio.h>
int main(void)
{
	int a,b,c,d,e,x[101],X[101],cnt=0,i,j,flg[101],S,s,w=0;
	scanf("%d",&a);
	for(i=1;i<=a;i++){
		x[i]=0;
		X[i]=i;
		flg[i]=0;
	}
	for(i=0;i<a*(a-1)/2;i++){
		scanf("%d%d%d%d",&b,&c,&d,&e);
		if(d>e){
			x[b]+=3;
		}
		else if(e>d){
			x[c]+=3;
		}
		else{
			x[b]++;
			x[c]++;
		}
	}
	for(i=1;i<=a;i++){
		s=-1;
		S=0;
		for(j=1;j<=a;j++){
			if(flg[j]==0){
				if(s<x[j]){
					s=x[j];
					S=j;
				}
			}
		}
		flg[S]=1;
		if(w==s){
			cnt++;
			X[S]=i-cnt;
		}
		else{
			cnt=0;
			X[S]=i;
		}
		w=s;
	}
	for(i=1;i<=a;i++){
		printf("%d\n",X[i]);
	}
	return 0;
}